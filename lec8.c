void construct_key_schedule() {
    char KPuls[56];
    int i, j;
    int index=0;
    for(i=0; i<8; i++) {
        for(j=0; j<7; j++) {
            KPuls[index] = K[PC_1[i][j]-1];
            index++;
        }
    }
    
    char C[17][28];
    char D[17][28];
    for(int index=0; index<17; index++) {
        int k = ROL[index];
        for(int j=0; j<28; j++, k++) {
            if(index == 0) {
                if(k >= 28) k -= 28;
                C[index][j] = KPuls[k];
                D[index][j] = KPuls[k+28];
            } else {
                if(k >= 28) k -= 28;
                C[index][j] = C[index-1][k];
                D[index][j] = D[index-1][k];
            }
        }
    }
    
    for(int index=0; index<16; index++) {
        int length=0;
        for(i=0; i<8; i++) {
            for(j=0; j<6; j++) {
                if(PC_2[i][j] < 29) {
                    keys[index][length] = C[index+1][PC_2[i][j]-1];
                } else {
                    keys[index][length] = D[index+1][PC_2[i][j]-29];
                }
                length++;
            }
        }
    }
}

